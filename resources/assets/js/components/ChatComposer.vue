<template lang="html">
    <div class="chat-composer">
        <input type="text" placeholder="Wiadomość" v-model="messageText"  v-on:keyup.enter="sendMessage"/>
        <button class="btn btn-primary" v-on:click="sendMessage">Wyślij</button>
    </div>
</template>

<script>
    export default {
        data(){
            return {
                messageText:'',
            }
        },
        methods:{
            sendMessage:function(){
                this.$emit('messagesent',{
                    text: this.messageText
                });
                this.messageText="";
            }
        }
    }
</script>


<style lang="css">
    .chat-composer {
        display: flex;

    }

    .chat-composer input{
        flex: 1 auto;

    }
    .chat-composer button{
       border-radius: 0;

    }
</style>